#include "libft.h"

void	ft_arrfree(char ***arr)
{
	int i;

	i = 0;
	if (arr && *arr)
	{
		while (*(*arr + i))
			ft_strdel(*arr + i++);
		free(*arr);
		*arr = NULL;
	}
}
