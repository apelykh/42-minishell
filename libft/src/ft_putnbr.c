#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
		ft_putchar('-');
	if (ft_abs(n) >= 0 && ft_abs(n) <= 9)
		ft_putchar(ft_abs(n) + '0');
	else
	{
		ft_putnbr(ft_abs(n / 10));
		ft_putchar(ft_abs(n % 10) + '0');
	}
}
