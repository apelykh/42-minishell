#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *res;

	res = s1;
	while (*res)
		res++;
	while (n-- > 0 && *s2)
		*res++ = *s2++;
	*res = '\0';
	return (s1);
}
